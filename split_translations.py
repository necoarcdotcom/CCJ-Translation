def classify(line):
    s = line.rstrip('\n\r')
    if not s or '=' not in s:
        return 'gameplay'

    # 1) SYSTEM
    for kw in SYSTEM_KW:
        if kw in s:
            return 'system'

    # 2) TUTORIAL
    for kw in TUTORIAL_KW:
        if kw in s:
            return 'tutorial'

    # 3) MUSIC
    for kw in MUSIC_KW:
        if kw in s:
            return 'music'

    # 4) CHARACTER equipment / bios / family names
    for kw in CHARACTER_KW:
        if kw in s:
            return 'characters'

    # 5) MAIN SKILLS  (pink color tag)
    if 'color\\=#dc3096>' in s:
        return 'skills'

    # 6) PASSIVE ABILITIES  (cyan color tag)
    if 'color\\=#18bcf2>' in s:
        return 'abilities'

    # 7) TITLES
    for kw in TITLE_KW:
        if kw in s:
            return 'titles'
    if TITLE_COLOR.search(s):
        return 'titles'

    # 8) COSMETICS
    for kw in COSMETIC_KW:
        if kw in s:
            return 'cosmetics'

    # 9) MISSIONS
    for kw in MISSION_KW:
        if kw in s:
            return 'missions'

    # 10) GAMEPLAY  (catch-all)
    return 'gameplay'


for line in lines:
    cat = classify(line)
    buckets[cat].append(line)

# Write output files
for cat, content in buckets.items():
    fname = os.path.join(dst_dir, f'{cat}.txt')
    with open(fname, 'w', encoding='utf-8') as f:
        f.writelines(content)
    print(f'{cat}.txt: {len(content)} lines written')

total = sum(len(v) for v in buckets.values())
print(f'\nTotal lines processed: {total} / {len(lines)}')
